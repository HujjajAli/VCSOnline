package com.vcs.model;


public class Pet {
	
	   private String petID;
	   private String petName;
	   private Double petHeight;
	   private Double petWeight;
	   private Double petLength;
	   private String animalType;
	   private String breed;
	   private String petDOB;
	   private String petGender;
	   private Customer customer;
	public String getPetID() {
		return petID;
	}
	public void setPetID(String petID) {
		this.petID = petID;
	}
	public String getPetName() {
		return petName;
	}
	public void setPetName(String petName) {
		this.petName = petName;
	}
	public Double getPetHeight() {
		return petHeight;
	}
	public void setPetHeight(Double petHeight) {
		this.petHeight = petHeight;
	}
	public Double getPetWeight() {
		return petWeight;
	}
	public void setPetWeight(Double petWeight) {
		this.petWeight = petWeight;
	}
	public Double getPetLength() {
		return petLength;
	}
	public void setPetLength(Double petLength) {
		this.petLength = petLength;
	}
	public String getAnimalType() {
		return animalType;
	}
	public void setAnimalType(String animalType) {
		this.animalType = animalType;
	}
	public String getBreed() {
		return breed;
	}
	public void setBreed(String breed) {
		this.breed = breed;
	}
	public String getPetDOB() {
		return petDOB;
	}
	public void setPetDOB(String petDOB) {
		this.petDOB = petDOB;
	}
	public String getPetGender() {
		return petGender;
	}
	public void setPetGender(String petGender) {
		this.petGender = petGender;
	}
	public Customer getCustomer() {
		return customer;
	}
	public void setCustomer(Customer customer) {
		this.customer = customer;
	}
	   
	   

}
